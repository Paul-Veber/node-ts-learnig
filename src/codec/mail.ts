import * as io from 'io-ts'
import { withMessage } from 'io-ts-types'
import { codec_error } from '../utils/form_error_handling'

interface EmailAddressBrand {
    readonly EmailAddress: unique symbol
}

const rxEmail = /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/i
export const EmailAddress = withMessage(
    io.brand(
        io.string,
        (s: string): s is io.Branded<string, EmailAddressBrand> => rxEmail.test(s),
        'EmailAddress'
    ), input => codec_error(input, `Email non valide ${input}`)
)

export type EmailAddress = io.TypeOf<typeof EmailAddress>